package com.organOld.oService.contract;

public class Coordinate {
    private Integer id;
    private String cx;
    private String cy;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getcx() {
        return cx;
    }

    public void setcx(String cx) {
        this.cx = cx;
    }

    public String getcy() {
        return cy;
    }

    public void setcy(String cy) {
        this.cy = cy;
    }

}
